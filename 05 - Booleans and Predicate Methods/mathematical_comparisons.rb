# mathematical_comparisons.rb

# Basic mathematical comparisons
a = 10
b = 20

puts "a = #{a}, b = #{b}"
puts "a == b: #{a == b}"  # false
puts "a != b: #{a != b}"  # true
puts "a > b: #{a > b}"    # false
puts "a < b: #{a < b}"    # true
puts "a >= b: #{a >= b}"  # false
puts "a <= b: #{a <= b}"  # true
